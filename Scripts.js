const presaleAddress = '  0x2dB1d6fD8C07bD725C9208A087220bf9A2528261 ';
const tokenAddress = '0xEB5ae4B69c755dB67df765f1b9dab01a99175C5D';

async function connectWallet() {
    if (window.ethereum) {
        try {
            await window.ethereum.request({ method: 'eth_requestAccounts' });
            console.log('Wallet connected');
        } catch (error) {
            console.error('User denied account access');
        }
    } else {
        alert('MetaMask is not installed. Please install it to use this feature.');
    }
}

async function getContractInstances() {
    if (window.ethereum) {
        const web3 = new Web3(window.ethereum);
        const presaleContract = new web3.eth.Contract(presaleABI, presaleAddress);
        const tokenContract = new web3.eth.Contract(tokenABI, tokenAddress);
        return { presaleContract, tokenContract };
    }
}

async function buyTokens(amountInBNB) {
    const { presaleContract } = await getContractInstances();
    const accounts = await window.ethereum.request({ method: 'eth_requestAccounts' });
    const account = accounts[0];
    
    try {
        await presaleContract.methods.buyTokens().send({
            from: account,
            value: web3.utils.toWei(amountInBNB, 'ether')
        });
        alert('Purchase successful');
    } catch (error) {
        console.error('Error purchasing tokens:', error);
    }
}

document.getElementById('connectWallet').addEventListener('click', connectWallet);

document.getElementById('purchase-form').addEventListener('submit', async (event) => {
    event.preventDefault();
    const amount = document.getElementById('amount').value;
    await buyTokens(amount);
});